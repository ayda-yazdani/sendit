import { useRef, useState } from "react";
import { View, Text, StyleSheet, Share } from "react-native";
import { TasteProfileData } from "@/lib/ai/taste-engine";
import { Button } from "@/components/shared/Button";
import { theme } from "@/constants/Theme";

interface IdentityCardProps {
  boardName: string;
  identityLabel: string;
  profileData: TasteProfileData;
}

export function IdentityCard({ boardName, identityLabel, profileData }: IdentityCardProps) {
  const [shared, setShared] = useState(false);

  const handleShare = async () => {
    const activities = profileData.activity_types?.join(", ") || "";
    const aesthetic = profileData.aesthetic || "";
    const food = profileData.food_preferences?.join(", ") || "";
    const locations = profileData.location_patterns?.join(", ") || "";
    const humour = profileData.humour_style || "";
    const price = profileData.price_range || "";

    const text = `${boardName}\n"${identityLabel}"\n\n` +
      `Into: ${activities}\n` +
      `Aesthetic: ${aesthetic}\n` +
      (food ? `Food: ${food}\n` : "") +
      (locations ? `Where: ${locations}\n` : "") +
      (humour && humour !== "not enough data yet" ? `Humour: ${humour}\n` : "") +
      (price ? `Budget: ${price}\n` : "") +
      `\nGenerated by Sendit`;

    await Share.share({ message: text });
    setShared(true);
    setTimeout(() => setShared(false), 2000);
  };

  return (
    <View style={styles.wrapper}>
      {/* The visual card */}
      <View style={styles.card}>
        <View style={styles.topBar} />
        <Text style={styles.boardName}>{boardName}</Text>
        <Text style={styles.identity}>{identityLabel}</Text>

        <View style={styles.divider} />

        <View style={styles.traits}>
          {profileData.activity_types?.slice(0, 4).map((a, i) => (
            <View key={i} style={styles.traitPill}>
              <Text style={styles.traitText}>{a}</Text>
            </View>
          ))}
        </View>

        {profileData.aesthetic && (
          <Text style={styles.aesthetic}>"{profileData.aesthetic}"</Text>
        )}

        <View style={styles.footer}>
          {profileData.price_range && (
            <Text style={styles.footerText}>💰 {profileData.price_range}</Text>
          )}
          {profileData.location_patterns?.[0] && (
            <Text style={styles.footerText}>📍 {profileData.location_patterns[0]}</Text>
          )}
        </View>

        <Text style={styles.branding}>sendit</Text>
      </View>

      {/* Share button */}
      <Button
        title={shared ? "Shared!" : "Share Identity"}
        onPress={handleShare}
        variant="secondary"
        style={{ marginTop: 12 }}
      />
    </View>
  );
}

const styles = StyleSheet.create({
  wrapper: { marginTop: 8 },
  card: {
    backgroundColor: theme.colors.bgDark,
    borderRadius: 20,
    padding: 24,
    paddingTop: 0,
    overflow: "hidden",
  },
  topBar: { height: 4, backgroundColor: theme.colors.primary, marginHorizontal: -24, marginBottom: 20 },
  boardName: { fontSize: 13, color: theme.colors.textMuted, textTransform: "uppercase", letterSpacing: 2, marginBottom: 6, fontFamily: theme.fonts.semibold },
  identity: { fontSize: 24, fontFamily: theme.fonts.bold, color: theme.colors.text, lineHeight: 30, marginBottom: 16 },
  divider: { height: 1, backgroundColor: theme.colors.border, marginBottom: 16 },
  traits: { flexDirection: "row", flexWrap: "wrap", gap: 8, marginBottom: 14 },
  traitPill: { backgroundColor: "rgba(212,86,42,0.15)", borderRadius: 8, paddingHorizontal: 12, paddingVertical: 6 },
  traitText: { fontSize: 13, color: theme.colors.warm, fontFamily: theme.fonts.semibold },
  aesthetic: { fontSize: 15, color: theme.colors.textSecondary, fontStyle: "italic", marginBottom: 16, fontFamily: theme.fonts.regular },
  footer: { flexDirection: "row", gap: 16, marginBottom: 16 },
  footerText: { fontSize: 13, color: theme.colors.textMuted, fontFamily: theme.fonts.regular },
  branding: { fontSize: 11, color: "rgba(247,243,237,0.25)", textAlign: "right", letterSpacing: 2, fontFamily: theme.fonts.regular },
});
